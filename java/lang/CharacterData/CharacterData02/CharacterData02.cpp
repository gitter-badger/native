int CharacterData02::toLowerCase(int character) {
    int mapChar = character;
    int val = getProperties(character);

    if ((val & 0x00020000) != 0) {
        int offset = val << 5 >> (5+18);
        mapChar = character + offset;
    }
    return mapChar;
}

int CharacterData02::toUpperCase(int character) {
    int mapChar = character;
    int val = getProperties(character);

    if ((val & 0x00010000) != 0) {
        int offset = val  << 5 >> (5+18);
        mapChar =  character - offset;
    }
    return mapChar;
}

int CharacterData02::toTitleCase(int character) {
    int mapChar = character;
    int val = getProperties(character);

    if ((val & 0x00008000) != 0) {
        // There is a titlecase equivalent.  Perform further characterecks:
        if ((val & 0x00010000) == 0) {
            // The characteraracter does not have an uppercase equivalent, so it must
            // already be uppercase; so add 1 to get the titlecase form.
            mapChar = character + 1;
        }
        else if ((val & 0x00020000) == 0) {
            // The characteraracter does not have a lowercase equivalent, so it must
            // already be lowercase; so subtract 1 to get the titlecase form.
            mapChar = character - 1;
        }
        // else {
        // The characteraracter has both an uppercase equivalent and a lowercase
        // equivalent, so it must itself be a titlecase form; return it.
        // return character;
        //}
    }
    else if ((val & 0x00010000) != 0) {
        // This characteraracter has no titlecase equivalent but it does have an
        // uppercase equivalent, so use that (subtract the signed case offset).
        mapChar = toUpperCase(character);
    }
    return mapChar;
}

int CharacterData02::digit(int character, int radix) {
    int value = -1;
    if (radix >= Character::MIN_RADIX && radix <= Character::MAX_RADIX) {
        int val = getProperties(character);
        int kind = val & 0x1F;
        if (kind == Character::DECIMAL_DIGIT_NUMBER) {
            value = character + ((val & 0x3E0) >> 5) & 0x1F;
        }
        else if ((val & 0xC00) == 0x00000C00) {
            // Java supradecimal digit
            value = (character + ((val & 0x3E0) >> 5) & 0x1F) + 10;
        }
    }
    return (value < radix) ? value : -1;
}

int CharacterData02::getNumericValue(int character) {
    int val = getProperties(character);
    int retval = -1;

    switch (val & 0xC00) {
        default: // cannot occur
        case (0x00000000):         // not numeric
            retval = -1;
            break;
        case (0x00000400):              // simple numeric
            retval = character + ((val & 0x3E0) >> 5) & 0x1F;
            break;
        case (0x00000800):       // strange numeric
            retval = -2;
            break;
        case (0x00000C00):           // Java supradecimal
            retval = (character + ((val & 0x3E0) >> 5) & 0x1F) + 10;
            break;
    }
    return retval;
}

