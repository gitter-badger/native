int CharacterData02::getProperties(int character) {
    auto offset = (char16_t)character;
    int indexX = offset >> 5;
    int indexY = X[indexX] | ((offset >> 1) & 0xF);
    int props = A[ Y[indexY] |(offset & 0x1)];
    return props;
}

