int CharacterData00::findInCharMap(int character) {
    int top, bottom, current;
    bottom = 0;
    top = 103; // charMap length
    current = top/2;
    // invariant: top > current >= bottom && ch >= CharacterData.charMap[bottom][0]
    while (top - bottom > 1) {
        if (character >= charMap[current][0][0]) {
            bottom = current;
        } else {
            top = current;
        }
        current = (top + bottom) / 2;
    }
    if (character == charMap[current][0][0]) {
        return current;
    }
    return -1;
}

Array<int> CharacterData00::initA() {
    Array<int> result;
    int i = 0;
    while (i < (930 * 2)) {
        int entry = A_DATA[i++] << 16;
        result.push(entry | A_DATA[i++]);
    }
    return result;
}
